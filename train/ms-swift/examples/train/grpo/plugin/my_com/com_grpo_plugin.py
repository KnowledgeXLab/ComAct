class COMMultiTurnScheduler(MultiTurnScheduler):

    def check_finished(self, infer_request, response_choice, current_turn: int) -> bool:
        content = response_choice.message.content or ""
        
        decision = _extract_decision(content)
        
        if decision and ('DONE' in decision.upper() or 'FAIL' in decision.upper()):
            return True

        return super().check_finished(infer_request, response_choice, current_turn)


    def step(self, infer_request, response_choice, current_turn: int) -> Dict:
        content = response_choice.message.content or ""

        def _run_env_sync():
            """Closure to run async virtual machines in a synchronous function"""
            loop = asyncio.new_event_loop()
            loop.set_default_executor(ThreadPoolExecutor(max_workers=256))
            # asyncio.set_event_loop(loop)
            
            try:
                # ==========================================
                # State Machine: Turn 1, Reset then Step
                # ==========================================
                if current_turn == 1:
                    # 1. Fetch the exclusive VM assigned to the current task from the queue manager
                    env_id = mycom_env_queue.get()

                    infer_request.data_dict['env_id'] = env_id
                    env = mycom_envs[str(env_id)]
                    env.task_config = infer_request.data_dict
                    
                    # 2. Reset the environment (equivalent to clearing files and opening CAD software)
                    loop.run_until_complete(env.reset(infer_request))
                    
                    # 3. Immediately execute the first code block generated by the LLM
                    next_obs, reward, terminated, done, fail, step_info = loop.run_until_complete(env.step(content))
                    return env_id, next_obs
                
                # ==========================================
                # State Machine: Subsequent Turns, Step Directly
                # ==========================================
                else:
                    env_id = infer_request.data_dict['env_id']
                    env = mycom_envs[str(env_id)]
                    
                    # Execute the newly generated code directly
                    next_obs, reward, terminated, done, fail, step_info = loop.run_until_complete(env.step(content))
                    return env_id, next_obs
                    
            finally:
                loop.close()
                
        # 1. Run environment interaction (retrieve VM ID and latest screenshot)
        env_id, next_obs = _run_env_sync()
        
        # 2. Get the current conversation history
        messages = infer_request.messages
        
        # 3. Seal the envelope! Append the VM ID to the end of the model's response (for the Reward function to locate it)
        stamp = f"\n[ENV_ID:{env_id}]"
        # messages.append({'role': 'assistant', 'content': content + stamp})
        if messages[-1]['role'] == 'assistant':
            messages[-1]['content'] = messages[-1]['content']+stamp
        # response_choice.message.content = content + stamp
        
        terminal_output = next_obs.get('terminal', {})
        screenshot = next_obs.get('screenshot', b'')
        img_b64_str = base64.b64encode(screenshot).decode('utf-8') if screenshot else ""
        
        if screenshot:
            import time
            sample_id = infer_request.data_dict.get('id', 'unknown')
            rank = os.environ.get('RANK', '0')
            timestamp = time.strftime('%m%d_%H%M%S')
            # Save the latest CAD screenshot to the local server
            img_path = f"/nvme/aijiaxin/cache/msswift/screenshot/com_env{env_id}_turn{current_turn}_sample{sample_id}_rank{rank}_timestamp{timestamp}.png"

            with open(img_path, "wb") as f:
                f.write(screenshot)
            infer_request.images.append(img_path)
            
            messages.append({
                "role": "user",
                "content": f"- Current Screenshot: <image>\n- Previous Terminal output: {terminal_output}"
            })
        else:
            messages.append({
                "role": "user",
                "content": f"- Previous Terminal output: {terminal_output}"
            })

        return {"infer_request": infer_request}



multi_turns["com_scheduler"] = COMMultiTurnScheduler


