def _extract_py_code(text: str) -> Optional[str]:
    """Extract the last ```python``` block"""
    matches = re.findall(r'```python\s*(.*?)\s*```', text, re.DOTALL)
    if matches:
        return matches[-1].strip()
    else:
        return None

def _extract_decision(text: str) -> Optional[str]:
    """Extract the last ```decision``` block"""
    matches = re.findall(r'```decision\s*(.*?)\s*```', text, re.DOTALL)
    if matches:
        return matches[-1].strip()
    else:
        return None

