class COMChamferReward(ORM):
    """
    Global reward calculation class specifically for COM tasks.
    Responsible for receiving LLM outputs, matching virtual machines, 
    downloading and verifying CAD physical Chamfer Distance.
    """

    def _evaluate_single(self, msg_list) -> float:
        """
        Single-sample scoring logic invoked by the thread pool.
        """
        reward_score = 0.0
        env_id = None
        
        try:
            # ==========================================
            # 1. Get the last message (used to determine if it is a FAIL)
            # ==========================================
            last_content = ""
            for msg in reversed(msg_list):
                if msg.get("role") == "assistant":
                    last_content = msg.get("content", "")
                    if isinstance(last_content, list):
                        last_content = " ".join(c.get("text", "") for c in last_content if c.get("type") == "text")
                    break  # Break once the last message is found
            
            # ==========================================
            # 2. Search backward for the environment ID stamp (as the last sentence might be an unsealed DONE)
            # ==========================================
            for msg in reversed(msg_list):
                if msg.get("role") == "assistant":
                    content_str = msg.get("content", "")
                    if isinstance(content_str, list):
                        content_str = " ".join(c.get("text", "") for c in content_str if c.get("type") == "text")
                    
                    match = re.search(r'\[ENV_ID:\s*(\d+)\]', content_str)
                    if match:
                        env_id = int(match.group(1))
                        break  # Found the actual stamped ID, break the search loop

            # If no ID stamp is found across the history (implies a crash before code generation in Turn 1)
            if env_id is None:
                return 0.0
                
            env = mycom_envs[str(env_id)]
            
            # ==========================================
            # 3. Reward Calculation Logic
            # ==========================================
            decision = _extract_decision(last_content)
            if decision and 'FAIL' in decision.upper():
                reward_score = 0.0
            else:
                task_type = env.task_config.get('task_type', '')
                if task_type in ['3d_model', 'assembly']:
                    reward_score, _ = env.evaluate_3d_model()
                elif task_type == '2d_sketch':
                    reward_score, _ = env.evaluate_2d_sketch()
                else:
                    reward_score = 0.0
                    
        except Exception as e:
            logger.error(f"Evaluation Error: {e}")
            reward_score = 0.0
            
        finally:
            # Fallback to 0.0 score for any exceptions
            if env_id is not None:
                mycom_env_queue.put(env_id)
                
        return reward_score


    def __call__(self, completions, messages, **kwargs) -> List[float]:
        # ==============================================================
        # High-speed concurrent launch via ThreadPoolExecutor!
        # Set max_workers to len(messages) to ensure an independent thread for each data sample.
        # ==============================================================
        with concurrent.futures.ThreadPoolExecutor(max_workers=len(messages)) as executor:
            rewards = list(executor.map(self._evaluate_single, messages))
            
        return rewards


orms["com_reward"] = COMChamferReward